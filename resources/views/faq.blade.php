    <h1>Questions and Answers</h1>

    <h3>What fees does Nest charge my customers?</h3>
    <p>Nest doesn't charge your customers!  There is a processing fee added at checkout, which pays for credit card processing.  This fee is determined by the processing company (currently 2.6% + .30 per transaction).  This fee is always at cost, not marked up.</p>

    <h3>What percentage of the rental fee does Nest keep?</h3>
    <p>0%  Nest is not interested in charging you or your customers in any way that isn't necessary.</p>

    <h3>Is there an on-boarding / startup fee?</h3>
    <p>No, there is no on-boarding fee.  We do charge hourly if we help you migrate your properties from another system, but this is a voluntary service that we offer, not a requirement.</p>

    <h3>Why is my unit not showing on the Nest website?</h3>
    <p>We don't show your properties on our website so that we don't compete with your business.  We host 'em, you rent 'em!  We do advertise your company on our site, with a link to your property search to help with your Search Engine Optimization (SEO).  You can also ask Nest to show your properties on the Nest website if you don't have one (this setting is in your Company Settings).</p>

    <h3>Why is there a limit to the number of complexes, units, and photos I can have?</h3>
    <p>Larger numbers of photos and information require more hardware and maintenance than smaller numbers of the same.  For example, a business with 50 units, each having 20 photos, requires more hardware resources than a company with one unit and three pictures.  For this reason, larger plans cost more to cover investments in technologies to keep Nest going.</p>

    <h3>Can we add features to Nest?</h3>
    <p>We plan on constantly expanding Nest to include features that it needs, or that will make your job easier.  We will send out newsletters with our planned features periodically, and we always welcome suggestions for new features.  Keep in mind, though, we're building Nest to fit everyone, so any new features have to be a benefit to the majority of users.</p>

    <h3>Why can't I upload videos?</h3>
    <p>Nest currently does not support video uploads due to technological limitations.  We hope to add this in the near future!</p>

    <h3>Is there an extra fee for having my units on my own website?</h3>
    <p>Nope!  Nest has a free WordPress plugin, and there's a free API that your developer can use to pull the properties onto non-WordPress sites.  We also have developers on-staff who can do custom integrations if you don't have one!</p>

    <h3>I don't have a website, can you build one?</h3>
    <p>We don't build websites, but we can definitely help you pick out a developer to fit your needs.</p>

    <h3>Why are my properties loading slowly on my website?</h3>
    <p>This could be due to several factors.  Make sure that you are uploading images that are optimized for websites!  If you're still having a problem, make sure your developer has optimized your site and that you are getting good scores on <a href="https://developers.google.com/speed/pagespeed/insights/" target="_blank">Google's PageSpeed Insights</a>.</p>

